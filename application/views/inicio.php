var last = "";
var idAux="";
function cleanLast(){
	  $("#"+idAux).css("overflow","hidden");
	  $("#"+idAux).css("cursor","pointer");
	  $("#"+idAux).find("p").css("background-color","#F2F2F2");
	  
	  idAux = "";
	  last ="";
	
}

function modal(unId){
  
  if(unId != last){
	  idAux=unId;
		$("#"+unId).find("p").css("background-color","white");

	  
  $("#"+unId).removeStyle("overflow");
  $("#"+unId).removeStyle("cursor");
	 Custombox.open({
         target: '#'+unId,
         effect: 'fadein',
         close : cleanLast
     });
  
  
  last = unId;
    
  }
	
}


